class p2_VariableSizes {
    public static void main(String[] args) {

        // Integers
        System.out.println("Integer \n\tSize = " + Integer.SIZE);
        System.out.println("\tMin = " + Integer.MIN_VALUE + "\n\tMax = " + Integer.MAX_VALUE);

        //Replace Integer with any other variable type to get its size and min/max values
        //Available = numeric     -> byte(8), short(16), int(32), long(64)
        //                        -> float(32), double(64)
        //            non-numeric -> char(16), boolean(1)
    }
}
